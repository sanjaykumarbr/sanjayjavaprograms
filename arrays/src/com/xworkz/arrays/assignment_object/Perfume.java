package com.xworkz.arrays.assignment_object;

public class Perfume {
	
	String name;
	String color;
	
	public Perfume(String name,String color) {
		this.name = name ;
		this.color = color;
	}

	@Override
	public String toString() {
		return "Perfume [name=" + name + ", color=" + color + "]";
	}
	
	
	


}
