package com.xworkz.arrays.assignment_object;

public class Blazer {
	
	String name;
	String color;
	
	public Blazer(String name,String color) {
		this.name = name ;
		this.color = color;
	}

	@Override
	public String toString() {
		return "Blazer [name=" + name + ", color=" + color + "]";
	}
	
	

}
