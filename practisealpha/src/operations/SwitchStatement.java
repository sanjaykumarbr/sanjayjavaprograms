package operations;

public class SwitchStatement {
	
	
	

	public static void main(String[] args) {
		
		int num =3;
		
		switch(num) {
		
		case 1: System.out.println("monday");
		break;
		
		case 2: System.out.println("tuesday");
		break;
		
		case 3: System.out.println("wednesday");
		break;
		
		default: System.out.println("no input");
		
		
		}

	}

}
